import 'package:flutter/foundation.dart';
import 'package:flutter/material.dart';

class AppLocalizations {
  const AppLocalizations(this.locale);

  final Locale locale;

  static const supportedLocales = [
    Locale('en'),
    Locale('hr'),
    Locale('de'),
    Locale('es'),
    Locale('fr'),
    Locale('it'),
    Locale('ja'),
    Locale('zh'),
  ];

  static const delegate = _AppLocalizationsDelegate();

  static AppLocalizations of(BuildContext context) {
    return Localizations.of<AppLocalizations>(context, AppLocalizations)!;
  }

  static String languageName(Locale? locale) {
    return switch (locale?.languageCode) {
      null => 'System',
      'hr' => 'Hrvatski',
      'de' => 'Deutsch',
      'es' => 'Español',
      'fr' => 'Français',
      'it' => 'Italiano',
      'ja' => '日本語',
      'zh' => '中文',
      _ => 'English',
    };
  }

  String t(String key) {
    final language = _strings[locale.languageCode] ?? _strings['en']!;
    return language[key] ?? _strings['en']![key] ?? key;
  }

  String gameName(String key, String fallback) =>
      t('game.$key.name') == 'game.$key.name' ? fallback : t('game.$key.name');

  String gameSubtitle(String key, String fallback) =>
      t('game.$key.subtitle') == 'game.$key.subtitle'
      ? fallback
      : t('game.$key.subtitle');
}

class _AppLocalizationsDelegate
    extends LocalizationsDelegate<AppLocalizations> {
  const _AppLocalizationsDelegate();

  @override
  bool isSupported(Locale locale) {
    return AppLocalizations.supportedLocales.any(
      (supported) => supported.languageCode == locale.languageCode,
    );
  }

  @override
  Future<AppLocalizations> load(Locale locale) {
    final supported = AppLocalizations.supportedLocales.firstWhere(
      (item) => item.languageCode == locale.languageCode,
      orElse: () => const Locale('en'),
    );
    return SynchronousFuture(AppLocalizations(supported));
  }

  @override
  bool shouldReload(_AppLocalizationsDelegate old) => false;
}

